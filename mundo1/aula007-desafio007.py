#Desenvolva um programa que leia as duas notas de um aluno, calcule e
# mostre a sua media(ordem de precedência)
nota1 = float(input('Prezade estudante, digite uma nota:'))
nota2 = float(input('Agora digite outra nota:'))
m = (nota1+nota2)/2
print(f'O resultado da média entre as notas digitadas é: {m:.1f}. ')